class Solution {
  public:
    int getSecondLargest(std::vector<int> &arr) {
        int largest = -1, secondLargest = -1;

        for (int num : arr) {
            if (num > largest) {
                secondLargest = largest;
                largest = num;
            } else if (num > secondLargest && num < largest) {
                secondLargest = num;
            }
        }

        return secondLargest;
    }
};

